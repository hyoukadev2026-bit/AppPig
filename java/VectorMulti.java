class VectorMulti {
    double d1, d2, x, y, r, angulo;
    String tipoOriginal;

    public VectorMulti(double d1, double d2, double x, double y, double r, double angulo, String tipo) {
        this.d1 = d1;
        this.d2 = d2;
        this.x = x;
        this.y = y;
        this.r = r;
        this.angulo = angulo;
        this.tipoOriginal = tipo;
    }
}
